import pytest
import hmac
import hashlib
import time


def generate_signature(api_key: str, timestamp: int, secret_key: str) -> str:
    """Generate HMAC-SHA256 signature for Partner API auth"""
    message = f"{api_key}{timestamp}"
    return hmac.new(
        secret_key.encode(),
        message.encode(),
        hashlib.sha256
    ).hexdigest()


