class TestPartnerQuery:
    """Test partner debt query endpoints"""

    def test_partner_query_with_valid_token(self, client, sample_partner, sample_debtor):
        """Test partner query with valid signature auth"""
        timestamp = int(time.time())
        signature = generate_signature(
            sample_partner.api_key,
            timestamp,
            sample_partner.secret_key if hasattr(sample_partner, 'secret_key') else "default_secret"
        )
        
        response = client.post(
            "/api/partner/query",
            headers={
                "Authorization": f"Partner {sample_partner.api_key}:{signature}:{timestamp}"
            },
            json={
                "id_card": sample_debtor.id_card,
                "name": sample_debtor.name
            }
        )
        # 200 if found, 403 if name doesn't match
        assert response.status_code in [200, 403]

    def test_partner_query_without_token(self, client):
        """Test partner query without token"""
        response = client.post(
            "/api/partner/query",
            json={"id_card": "110101199001011234", "name": "Test"}
        )
        assert response.status_code == 401

    def test_partner_query_with_invalid_token(self, client):
        """Test partner query with invalid token"""
        response = client.post(
            "/api/partner/query",
            headers={"Authorization": "Bearer invalid_token"},
            json={"id_card": "110101199001011234", "name": "Test"}
        )
        assert response.status_code == 401


